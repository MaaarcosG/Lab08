// funcion la suma
function sum(a, b) {
  return a + b;
}
export { sum };

// funcion de resta
function sub(a, b) {
  return a - b;
}
export { sub };

// funcion de multiplicacion
function mult(a, b) {
  return a * b;
}
export { mult };

// funcion de division
function div(a, b) {
  return Math.round(a / b);
}
export { div };

// funcion de mod
function modulo(a, b) {
  return a % b;
}
export { modulo };
